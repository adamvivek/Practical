class Simple_ifelseladder
{
public static void main(String args[])
{

String name = "V";

if(name == "A")
{
System.out.println("Name is A");
}
else if (name == "B")
{
System.out.println("Name is B");
}
else if (name == "C")
{
System.out.println("Name is C");
}
else{
System.out.println(name);
}}}
